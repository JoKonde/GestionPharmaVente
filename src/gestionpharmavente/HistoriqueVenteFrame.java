    private void btnSupprimerApproMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSupprimerApproMouseClicked
        // TODO add your handling code here:
        int row = tableAppro.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Sélectionnez une vente à supprimer.",
                    "Aucune vente sélectionnée", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int idVente = Integer.parseInt(tableAppro.getValueAt(row, 0).toString());
        int confirm = JOptionPane.showConfirmDialog(this,
                "Supprimer cette vente ? Le stock des produits sera augmenté des quantités correspondantes.",
                "Confirmation", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        try {
            // Récupère les lignes avant suppression pour ajuster le stock
            List<LigneVente> lignes = ligneVenteDAO.listerParVente(idVente);
            for (LigneVente l : lignes) {
                // Annulation de la vente = retour en stock
                produitDAO.mettreAJourStock(l.getIdProd(), l.getQte());
            }
            venteDAO.supprimerVente(idVente);
            chargerVentes();
            JOptionPane.showMessageDialog(this, "Vente supprimée.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la suppression de la vente : " + ex.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSupprimerApproMouseClicked

    /**
     * Affiche une "facture" formatée pour la vente sélectionnée.
     * Utilise les données déjà présentes dans la table (date, ref) et les lignes de vente.
     */
    private void btnFacMouseClicked(java.awt.event.MouseEvent evt) {
        int row = tableAppro.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Sélectionnez une vente pour voir la facture.",
                    "Aucune vente sélectionnée", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int idVente = Integer.parseInt(tableAppro.getValueAt(row, 0).toString());
        String dateStr = tableAppro.getValueAt(row, 1).toString();
        String ref = tableAppro.getValueAt(row, 2).toString();
        try {
            List<LigneVente> lignes = ligneVenteDAO.listerParVente(idVente);
            if (lignes.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Aucune ligne pour cette vente.",
                        "Facture", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            StringBuilder sb = new StringBuilder();
            // En-tête société
            sb.append("SOCIETE X\n");
            sb.append("Avenue Y, Ville Z\n");
            sb.append("Tél: 000 000 000\n\n");

            // Informations facture
            sb.append("FACTURE DE VENTE\n");
            sb.append("Numéro: ").append(ref).append("\n");
            sb.append("Date  : ").append(dateStr).append("\n\n");

            // Tableau des lignes
            sb.append(String.format("%-25s %8s %12s %12s%n", "Produit", "Qté", "P.V. (FC)", "Total (FC)"));
            sb.append("---------------------------------------------------------------------\n");

            double totalGeneral = 0.0;
            for (LigneVente l : lignes) {
                Produit p = produitDAO.trouverParId(l.getIdProd());
                String nomProd = (p != null) ? p.getNom() : ("ID " + l.getIdProd());
                int qte = l.getQte();
                double pv = l.getPrixVente();
                double totalLigne = qte * pv;
                totalGeneral += totalLigne;

                sb.append(String.format("%-25s %8d %12.2f %12.2f%n",
                        nomProd, qte, pv, totalLigne));
            }

            sb.append("---------------------------------------------------------------------\n");
            sb.append(String.format("%-47s %12.2f%n", "Total HT (FC):", totalGeneral));
            sb.append(String.format("%-47s %12.2f%n", "Taxe (FC):", 0.0));
            sb.append(String.format("%-47s %12.2f%n", "Montant payé (FC):", totalGeneral));

            JOptionPane.showMessageDialog(this, sb.toString(), "Facture de vente",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur chargement des lignes de la vente : " + ex.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDetailsMouseClicked
        // TODO add your handling code here:
        int row = tableAppro.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Sélectionnez une vente pour voir les détails.",
                    "Aucune vente sélectionnée", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int idVente = Integer.parseInt(tableAppro.getValueAt(row, 0).toString());
        try {
            List<LigneVente> lignes = ligneVenteDAO.listerParVente(idVente);
            if (lignes.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Aucune ligne pour cette vente.",
                        "Détails", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            StringBuilder sb = new StringBuilder();
            sb.append("Lignes pour vente ID ").append(idVente).append(":\n");
            for (LigneVente l : lignes) {
                Produit p = produitDAO.trouverParId(l.getIdProd());
                String nomProd = (p != null) ? p.getNom() : ("ID " + l.getIdProd());
                sb.append("- ").append(nomProd)
                        .append(" | Qte: ").append(l.getQte())
                        .append(" | Prix vente: ").append(l.getPrixVente())
                        .append("\n");
            }
            JOptionPane.showMessageDialog(this, sb.toString(), "Détails vente",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur chargement détails de la vente : " + ex.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDetailsMouseClicked

