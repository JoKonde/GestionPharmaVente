    private void chargerVentes() {
        try {
            List<Vente> list = venteDAO.listerTous();
            DefaultTableModel model = (DefaultTableModel) tableAppro.getModel();
            model.setRowCount(0);
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy HH:mm");
            for (Vente v : list) {
                Date d = v.getDateVente();
                model.addRow(new Object[]{
                    v.getId(),
                    sdf.format(d),
                    v.getRef()
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur chargement des ventes : " + ex.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }
