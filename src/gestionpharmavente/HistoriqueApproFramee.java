    private void chargerApprovisionnements() {
        try {
            List<Appro> list = approDAO.listerTous();
            DefaultTableModel model = (DefaultTableModel) tableAppro.getModel();
            model.setRowCount(0);
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy HH:mm");
            for (Appro a : list) {
                Date d = a.getDateAppro();
                model.addRow(new Object[]{
                    a.getId(),
                    sdf.format(d),
                    a.getRef()
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur chargement approvisionnements : " + ex.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }
