    private void btnSupprimerApproMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSupprimerApproMouseClicked
        // TODO add your handling code here:
        int row = tableAppro.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un approvisionnement à supprimer.",
                    "Aucun approvisionnement sélectionné", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int idAppro = Integer.parseInt(tableAppro.getValueAt(row, 0).toString());
        int confirm = JOptionPane.showConfirmDialog(this,
                "Supprimer cet approvisionnement ? Le stock des produits sera diminué des quantités correspondantes.",
                "Confirmation", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        try {
            // Récupère les lignes avant suppression pour ajuster le stock
            List<LigneAppro> lignes = ligneApproDAO.listerParAppro(idAppro);
            for (LigneAppro l : lignes) {
                produitDAO.mettreAJourStock(l.getIdProd(), -l.getQte());
            }
            approDAO.supprimerAppro(idAppro);
            chargerApprovisionnements();
            JOptionPane.showMessageDialog(this, "Approvisionnement supprimé.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la suppression de l'approvisionnement : " + ex.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSupprimerApproMouseClicked

    private void btnDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDetailsMouseClicked
        // TODO add your handling code here:
        int row = tableAppro.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un approvisionnement pour voir les détails.",
                    "Aucun approvisionnement sélectionné", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int idAppro = Integer.parseInt(tableAppro.getValueAt(row, 0).toString());
        try {
            List<LigneAppro> lignes = ligneApproDAO.listerParAppro(idAppro);
            if (lignes.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Aucune ligne pour cet approvisionnement.",
                        "Détails", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            StringBuilder sb = new StringBuilder();
            sb.append("Lignes pour appro ID ").append(idAppro).append(":\n");
            for (LigneAppro l : lignes) {
                Produit p = produitDAO.trouverParId(l.getIdProd());
                String nomProd = (p != null) ? p.getNom() : ("ID " + l.getIdProd());
                sb.append("- ").append(nomProd)
                        .append(" | Qte: ").append(l.getQte())
                        .append(" | Prix achat: ").append(l.getPrixAchat())
                        .append("\n");
            }
            JOptionPane.showMessageDialog(this, sb.toString(), "Détails approvisionnement",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur chargement détails : " + ex.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDetailsMouseClicked

